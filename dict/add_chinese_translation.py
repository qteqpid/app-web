def update_chinese_translation():
    """更新missing_words.json中的中文翻译"""
    
    print("正在读取 missing_words.json...")
    with open('missing_words.json', 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    print(f"读取到 {len(data)} 个单词")
    
    # 更新每个单词的中文翻译
    for i, word_entry in enumerate(data):
        word = word_entry['character']
        translation = get_chinese_translation(word)
        data[i]['phrase'] = translation
        print(f"更新 {i+1:3d}/{len(data)}: {word} -> {translation}")
    
    # 保存更新后的文件
    with open('missing_words.json', 'w', encoding='utf-8') as f:
        f.write('[\n')
        for i, entry in enumerate(data):
            line = json.dumps(entry, ensure_ascii=False, separators=(',', ':'))
            if i < len(data) - 1:
                line += ','
            f.write(line + '\n')
        f.write(']\n')
    
    print(f"\n已更新 {len(data)} 个单词的中文翻译")
    print("文件已保存为 missing_words.json")

if __name__ == "__main__":
    update_chinese_translation() 